CREATE TABLE clients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  phone VARCHAR(255) NOT NULL,
  "documentID" VARCHAR(255) NOT NULL,
  email VARCHAR(255) NOT NULL,
  "creditLimit" VARCHAR(255) NOT NULL,
  status VARCHAR(255) NOT NULL,
  "createAt" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "createBy" VARCHAR(255) NOT NULL,
  carrer VARCHAR(255),
  balance NUMERIC(10,2) NOT NULL,
  uid VARCHAR(1000) NOT NULL
);
CREATE TABLE company (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255),
  rif VARCHAR(255),
  phone VARCHAR(255),
  ticket NUMERIC(10,2) NOT NULL,
  tasa NUMERIC(10,2) NOT NULL,
  account VARCHAR(2000),
  "phoneAccount" VARCHAR(255),
  "rifAccount" VARCHAR(255)
);
CREATE TABLE units (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  number VARCHAR(255) NOT NULL,
  plate VARCHAR(255) NOT NULL,
  status INTEGER NOT NULL,
  driver VARCHAR(255) NOT NULL
);
CREATE TABLE recharge (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  idclient INTEGER NOT NULL REFERENCES clients(id),
  method VARCHAR(255) NOT NULL,
  ref VARCHAR(255),
  picture VARCHAR(1000),
  amount NUMERIC(10,2) NOT NULL,
  tasa NUMERIC(10,2),
  date DATE NOT NULL,
  status INTEGER NOT NULL,
  "createBy" VARCHAR(255),
  "createAt" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  "updateAprobate" TIMESTAMP
);
CREATE TABLE solicitude (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  date VARCHAR(11) NOT NULL,
  idclient INTEGER NOT NULL REFERENCES clients(id),
  shedule VARCHAR(255) NOT NULL,
  route VARCHAR(255)
);
CREATE TABLE transactions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  uid VARCHAR(255) NOT NULL,
  idclient INTEGER NOT NULL REFERENCES clients(id),
  "createBy" INTEGER NOT NULL REFERENCES clients(id),
  amount NUMERIC(10,2) NOT NULL,
  status INTEGER NOT NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  idunit INTEGER NOT NULL REFERENCES units(id),
  shedule VARCHAR(255),
  "newBalanceClient" NUMERIC(10,2)
);


